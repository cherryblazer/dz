def num_translate(x):
    if(x == "zero"):
        print("ноль")
    if(x == "one"):
        print("один")
    if(x == "two"):
        print("два")
    if(x == "three"):
        print("три")
    if(x == "four"):
        print("четыре")
    if(x == "five"):
        print("пять")
    if(x == "six"):
        print("шесть")
    if(x == "seven"):
        print("семь")
    if(x == "eight"):
        print("восемь")
    if(x == "nine"):
        print("девять")
    if(x == "ten"):
        print("десять")
    
num_translate(input())